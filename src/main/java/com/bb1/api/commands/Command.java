package com.bb1.api.commands;

import java.util.Set;

import org.jetbrains.annotations.NotNull;

import com.bb1.api.commands.tab.ITabable;

public abstract class Command implements CommandHandler {
	
	protected final String name;
	
	public Command(@NotNull String name) { this.name = name; }
	
	public final String getName() { return this.name; }
	
	public String getDescription() { return getName(); }
	
	public String getCommandUsageAutoGenerated() {
		String s = "/"+getName();
		if (getParams()==null || getParams().length==0) {
			return s;
		}
		for (ITabable tab : getParams()) {
			s += " <"+tab.getTabableName()+">";
		}
		return s;
	}
	
	@Deprecated
	public String getCommandUsage(){ return null; };
	
	public Set<String> getAliases() { return null; }
	
	public String getPermission() { return null; }
	
	public boolean isPlayerOnlyCommand() { return false; }
	
	public ITabable[] getParams() { return null; }
	
	// REGISTERING STUFF
	
	public void register() {
		CommandManager.get().registerCommand(this);
	}
	
}
